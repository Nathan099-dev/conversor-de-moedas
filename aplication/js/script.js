function converterMoeda(){
    const valorInserido = document.querySelector('#valor_entrada_usuario');
    const moedaSelecionada = document.querySelector('select');

    if (moedaSelecionada == 'Dolar') {
        valorInserido * 6.01

    }else if (moedaSelecionada == 'Euro') {
        valorInserido * 6.37
    }

}